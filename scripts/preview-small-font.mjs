const xmlEsc = ch =>
  ({
    '"': '&quot;',
    "'": '&#39;',
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '⬡': '&#x2b21;',
    '◈': '&#x25c8;',
    '◉': '&#x25c9;',
    '⊙': '&#x2299;',
  })[ch] ?? ch;

const charW = COLS * CELL + GAP;
const charH = ROWS * CELL;
const rowH = charH + ROW_GAP + 10;

// ── Full character set preview ────────────────────────────────────────────────
const svgW = PAD * 2 + PER_ROW * charW;
const svgH = PAD * 2 + rows.length * rowH + 200; // +200 for sample text section

let content = '';
rows.forEach((row, ri) => {
  row.forEach((ch, ci) => {
    const xBase = PAD + ci * charW;
    const yBase = PAD + ri * rowH;
    const grid = glyphs[ch];
    if (!grid) return;
    grid.forEach((rowStr, rj) => {
      rowStr.split('').forEach((px, pj) => {
        if (px === '1') {
          content += `<rect x="${xBase + pj * CELL}" y="${yBase + rj * CELL}" width="${CELL - 2}" height="${CELL - 2}" fill="${ACCENT}"/>`;
        }
      });
    });
    content += `<text x="${xBase + (COLS * CELL) / 2 - 1}" y="${yBase + charH + 10}" font-size="7" fill="${LABEL}" text-anchor="middle" font-family="monospace">${xmlEsc(ch)}</text>`;
  });
});

// ── Sample text section — how it looks at actual chat sizes ──────────────────
const SAMPLE_Y = PAD + rows.length * rowH + 24;
const sampleSizes = [
  { label: '10px  —  dense UI', scale: 10 / (ROWS * CELL) },
  { label: '12px  —  chat body', scale: 12 / (ROWS * CELL) },
  { label: '14px  —  labels', scale: 14 / (ROWS * CELL) },
  { label: '18px  —  subheads', scale: 18 / (ROWS * CELL) },
];
const sampleText = 'based. your overly attached ai.';

function renderText(str, xOff, yOff, scale) {
  let x = xOff;
  let out = '';
  for (const ch of str) {
    if (ch === ' ') {
      x += Math.round(3 * scale);
      continue;
    }
    const grid = glyphs[ch] ?? glyphs['.'];
    const cw = Math.ceil(COLS * scale);
    const ch2 = Math.ceil(ROWS * scale);
    grid.forEach((rowStr, ri) => {
      rowStr.split('').forEach((px, pi) => {
        if (px === '1') {
          const rx = Math.round(x + pi * scale);
          const ry = Math.round(yOff + ri * scale);
          const sz = Math.max(1, Math.ceil(scale - 0.5));
          out += `<rect x="${rx}" y="${ry}" width="${sz}" height="${sz}" fill="${ACCENT}"/>`;
        }
      });
    });
    x += cw + Math.ceil(scale * 0.5);
    void ch2;
  }
  return out;
}

let sampleContent = '';
sampleSizes.forEach(({ label, scale }, i) => {
  const yOff = SAMPLE_Y + i * (Math.ceil(ROWS * sampleSizes[3].scale) + 24);
  sampleContent += `<text x="${PAD}" y="${yOff - 4}" font-size="8" fill="${LABEL}" font-family="monospace">${label}</text>`;
  sampleContent += renderText(sampleText, PAD, yOff, scale);
});

// ── Divider ──────────────────────────────────────────────────────────────────
content += `<line x1="${PAD}" y1="${SAMPLE_Y - 12}" x2="${svgW - PAD}" y2="${SAMPLE_Y - 12}" stroke="rgba(201,168,124,0.15)" stroke-width="1"/>`;
content += `<text x="${PAD}" y="${SAMPLE_Y - 2}" font-size="8" fill="${LABEL}" font-family="monospace">── sample text at actual render sizes ──</text>`;
content += sampleContent;

const totalH = SAMPLE_Y + sampleSizes.length * (Math.ceil(ROWS * sampleSizes[3].scale) + 24) + PAD;

const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" width="${svgW}" height="${totalH}" viewBox="0 0 ${svgW} ${totalH}">
  <rect width="100%" height="100%" fill="${BG}"/>
  ${content}
</svg>`;

writeFileSync('public/brand-font-small-preview.svg', svg);
