const cellSize = 100;
const unitsPerEm = 800;
const totalRows = 8;
const advanceWidth = 700;

function buildGlyphPath(rows) {
  const path = new opentype.Path();
  rows.forEach((rowStr, rowIdx) => {
    rowStr.split('').forEach((px, colIdx) => {
      if (px === '1') {
        const x = colIdx * cellSize;
        const y = (totalRows - 1 - rowIdx) * cellSize;
        path.moveTo(x, y);
        path.lineTo(x + cellSize, y);
        path.lineTo(x + cellSize, y - cellSize);
        path.lineTo(x, y - cellSize);
        path.closePath();
      }
    });
  });
  return path;
}

// .notdef glyph (empty)
const notdefGlyph = new opentype.Glyph({
  name: '.notdef',
  unicode: 0,
  advanceWidth,
  path: new opentype.Path(),
});

// Build all character glyphs
const glyphList = [notdefGlyph];
for (const [ch, rows] of Object.entries(glyphs)) {
  const unicode = ch.codePointAt(0);
  const path = buildGlyphPath(rows);
  const glyph = new opentype.Glyph({
    name: `uni${unicode.toString(16).toUpperCase().padStart(4, '0')}`,
    unicode,
    advanceWidth,
    path,
  });
  glyphList.push(glyph);
}

const font = new opentype.Font({
  familyName: 'BasedPixel',
  styleName: 'Regular',
  unitsPerEm,
  ascender: 700,
  descender: -100,
  glyphs: glyphList,
});

const arrayBuffer = font.toArrayBuffer();
const buffer = Buffer.from(new Uint8Array(arrayBuffer));
fs.writeFileSync('public/fonts/based-pixel.otf', buffer);
console.log(
  `Saved public/fonts/based-pixel.otf — ${buffer.length} bytes — ${glyphList.length} glyphs`
);
