const unitsPerEm = 600;
const ascender = 600;
const descender = -100;
const advanceWidth = 500; // 4 cols × 100 + 100 gap
const cellSize = 100;

function buildPath(bitmap) {
  const path = new opentype.Path();
  bitmap.forEach((rowStr, row) => {
    rowStr.split('').forEach((px, col) => {
      if (px === '1') {
        const x = col * cellSize;
        const y = ascender - row * cellSize; // top edge of cell (font Y is upward)
        path.moveTo(x, y);
        path.lineTo(x + cellSize, y);
        path.lineTo(x + cellSize, y - cellSize);
        path.lineTo(x, y - cellSize);
        path.closePath();
      }
    });
  });
  return path;
}

// .notdef glyph (empty box outline)
const notdefPath = new opentype.Path();
notdefPath.moveTo(50, 0);
notdefPath.lineTo(450, 0);
notdefPath.lineTo(450, 500);
notdefPath.lineTo(50, 500);
notdefPath.closePath();
notdefPath.moveTo(100, 50);
notdefPath.lineTo(100, 450);
notdefPath.lineTo(400, 450);
notdefPath.lineTo(400, 50);
notdefPath.closePath();

const notdefGlyph = new opentype.Glyph({
  name: '.notdef',
  unicode: 0,
  advanceWidth,
  path: notdefPath,
});

// Space glyph
const spaceGlyph = new opentype.Glyph({
  name: 'space',
  unicode: 32,
  advanceWidth,
  path: new opentype.Path(),
});

// Build all glyphs
const glyphList = [notdefGlyph, spaceGlyph];

for (const [char, bitmap] of Object.entries(glyphs)) {
  const codePoint = char.codePointAt(0);
  const name =
    char.charCodeAt(0) < 128
      ? char.replace(/[^a-zA-Z0-9]/g, c => `uni${c.charCodeAt(0).toString(16).padStart(4, '0')}`)
      : `uni${codePoint.toString(16).padStart(4, '0')}`;

  const glyph = new opentype.Glyph({
    name,
    unicode: codePoint,
    advanceWidth,
    path: buildPath(bitmap),
  });
  glyphList.push(glyph);
}

const font = new opentype.Font({
  familyName: 'BasedPixelSmall',
  styleName: 'Regular',
  unitsPerEm,
  ascender,
  descender,
  glyphs: glyphList,
});

const arrayBuffer = font.toArrayBuffer();
const buffer = Buffer.from(new Uint8Array(arrayBuffer));
fs.writeFileSync('public/fonts/based-pixel-small.otf', buffer);

const sizeKb = (buffer.length / 1024).toFixed(1);
console.log(`Saved: public/fonts/based-pixel-small.otf — ${sizeKb} KB, ${glyphList.length} glyphs`);
